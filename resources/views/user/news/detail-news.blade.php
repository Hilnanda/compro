                                <p><strong>Tom Rhoton</strong> is the vice president of marketing at Zenger Folkman. He and
                                    his team are responsible for communication, messaging, lead acquisition, and press
                                    relations. With over twenty-five years of experience in field, channel, partner and
                                    product marketing, Tom has come to the conclusion that organizations require two things
                                    from their marketing teams: Data and Inspiration. Metrics are essential to making
                                    complex value-based marketing decisions for any company. But equally important is the
                                    ability to motivate those around you and to multiply their talents.</p>
                                <p>Tom’s experience comes from working with global companies in many different industries.
                                    As a marketer, he has the unique perspective that only comes from someone who has held
                                    multiple sales and sales management positions and understands what is required to
                                    achieve quota.</p>
                                <p>Prior to Zenger Folkman, Tom was Vice President of Marketing at EveryoneSocial, and Vice
                                    President of Global Direct and Marketing Operations at Symantec.</p>
                                <p>Tom holds a Bachelor of Science degree in Information Management from Brigham Young
                                    University, and an MBA from the University of Utah. His approach – data and inspiration
                                    – runs parallel to Zenger Folkman’s mission to help good managers become extraordinary.
